    private int buscarIdEspecialidad(String descripcion)
    {
        for(int i=0;i<lista.size();i++)
        {
            if(lista.getT(i).getDescripcion().equals(descripcion))
                return lista.getT(i).getIdEspecialidad();
        }
        return -1;
    }
