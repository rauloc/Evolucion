    private void btnagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnagregarActionPerformed
        // TODO add your handling code here:
        String nombres = txtnombres.getText().trim();
        String apellidos = txtapellidos.getText().trim();
        String usuario = txtusuario.getText().trim();
        String pass = txtpass.getText().trim();
        String estado = "A";
        int idespecialidad = buscarIdEspecialidad(cbespecialidad.getSelectedItem().toString());
        if(!nombres.equals("") && !apellidos.equals("") && !usuario.equals("") && !pass.equals("")
                && idespecialidad>-1)
        {
            Doctor d = new Doctor(0, idespecialidad, nombres, apellidos, usuario, pass, estado);
            if (DoctorDAL.getInstance().agregarDoctor(d) > 0) {
                JOptionPane.showMessageDialog(null, "Doctor Registrado");
                limpiar();
                habilitarTextos(false);
                habilitarBotones(true);
            } else {
                JOptionPane.showMessageDialog(null, "No se Registro en la Base de Datos", "Mensaje", JOptionPane.ERROR_MESSAGE);
                limpiar();
            }
        }
        else
            JOptionPane.showMessageDialog(null, "Ingrese Datos", "Mensaje", JOptionPane.WARNING_MESSAGE);

    }//GEN-LAST:event_btnagregarActionPerformed

