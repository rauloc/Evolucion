    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        // TODO add your handling code here:
        int row=tabla.getSelectedRow();
        if(row>-1)
        {
            int idcodigo=Integer.parseInt(tabla.getValueAt(row, 0).toString());
            UpdatePaciente updatePaciente=new UpdatePaciente(null, true, idcodigo);
            updatePaciente.setVisible(true);
            
        }
        else
            JOptionPane.showMessageDialog(null, "Seleccione una fila", "Mensaje", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btneditarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        int row=tabla.getSelectedRow();
        if(row>-1)
        {
            int idcodigo=Integer.parseInt(tabla.getValueAt(row, 0).toString());
            if(PacienteDAL.getInstance().eliminarPaciente(idcodigo,"I")>0)
                JOptionPane.showMessageDialog(null, "Paciente Eliminado");
            else
                JOptionPane.showMessageDialog(null, "Error de eliminacion", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(null, "Seleccione una fila", "Mensaje", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btneliminarActionPerformed

