    private Object[][] devolverdatos()
    {
        
        ResultSet rs=PacienteDAL.getInstance().mostrarPacientes();
        int n=PacienteDAL.getInstance().cantidadPacientes();
        labresultado.setText(String.valueOf(n));
        Object[][] datos=null;
        try {
            datos=new Object[n][9];
            int i=0;
            while(rs.next())
            {
                datos[i][0]=rs.getInt(1);
                datos[i][1]=rs.getDate(10);
                datos[i][2]=rs.getString(2)+" "+rs.getString(3);
                datos[i][3]=rs.getDate(5);
                datos[i][4]=rs.getString(7);
                datos[i][5]=rs.getString(8);
                datos[i][6]=rs.getString(9);
                datos[i][7]=rs.getString(4);
                datos[i][8]=rs.getString(14);
                i++;
            }
        } catch (Exception ex) {
            
        }
        return datos;
    }

