    private void btneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditarActionPerformed
        // TODO add your handling code here:
        int row=tabla.getSelectedRow();
        if(row>-1)
        {
            int idcodigo=Integer.parseInt(tabla.getValueAt(row, 0).toString());
            UpdateDoctor updateDoctor=new UpdateDoctor(null, true, idcodigo);
            updateDoctor.setVisible(true);
            
        }
        else
            JOptionPane.showMessageDialog(null, "Seleccione una fila", "Mensaje", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btneditarActionPerformed

    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        try {
            // TODO add your handling code here:
            String usuario=JOptionPane.showInputDialog("Ingrese Usuario");
            ResultSet rs=DoctorDAL.getInstance().buscarDoctorUsuario(usuario);
            if(rs.getRow()>0){
                modelo.setDataVector(devolverdatos2(rs,rs.getRow()), columna);
            }
            else
                JOptionPane.showMessageDialog(null, "Datos No encontrados", "Mensaje", JOptionPane.WARNING_MESSAGE);
        } catch (SQLException ex) {
            Logger.getLogger(frmListaDoctores.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        int row=tabla.getSelectedRow();
        if(row>-1)
        {
            int idcodigo=Integer.parseInt(tabla.getValueAt(row, 0).toString());
            if(DoctorDAL.getInstance().eliminarDoctor(idcodigo,"I")>0)
                JOptionPane.showMessageDialog(null, "Doctor Eliminado");
            else
                JOptionPane.showMessageDialog(null, "Error de eliminacion", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }
        else
            JOptionPane.showMessageDialog(null, "Seleccione una fila", "Mensaje", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btneliminarActionPerformed

