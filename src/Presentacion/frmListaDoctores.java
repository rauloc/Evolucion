    private Object[][] devolverdatos()
    {
        
        ResultSet rs=DoctorDAL.getInstance().mostrarDoctor();
        int n=DoctorDAL.getInstance().cantidadDoctores();
        labresultado.setText(String.valueOf(n));
        Object[][] datos=null;
        try {
            datos=new Object[n][5];
            int i=0;
            while(rs.next())
            {
                datos[i][0]=rs.getInt(1);
                datos[i][1]=rs.getString(2)+" "+rs.getString(3);
                datos[i][2]=rs.getString(4);
                datos[i][3]=rs.getString(6);
                datos[i][4]=rs.getString(5);
                i++;
            }
        } catch (Exception ex) {
            
        }
        return datos;
    }
